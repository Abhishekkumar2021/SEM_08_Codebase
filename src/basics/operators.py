# Arithmetic Operators
a = 10
b = 3

addition = a + b          # Addition
subtraction = a - b       # Subtraction
multiplication = a * b    # Multiplication
division = a / b          # Division
modulus = a % b           # Modulus
exponentiation = a ** b   # Exponentiation
floor_division = a // b   # Floor Division

# Comparison Operators
equal = (a == b)          # Equal to
not_equal = (a != b)      # Not equal to
greater_than = (a > b)    # Greater than
less_than = (a < b)       # Less than
greater_equal = (a >= b)  # Greater than or equal to
less_equal = (a <= b)     # Less than or equal to  

# Logical Operators
x = True
y = False

logical_and = x and y     # Logical AND
logical_or = x or y       # Logical OR
logical_not = not x       # Logical NOT

# Bitwise Operators
bitwise_and = a & b       # Bitwise AND
bitwise_or = a | b        # Bitwise OR
bitwise_xor = a ^ b       # Bitwise XOR
bitwise_not = ~a          # Bitwise NOT
left_shift = a << 1       # Left Shift
right_shift = a >> 1      # Right Shift

# Unary Operators
# Negation Operator
negation = -a              # Negation

# Identity Operator
identity = +b              # Identity